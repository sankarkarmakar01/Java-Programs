package others.practice;

public class Switch {
    public static void main(String[] args) {
        int n = 2;
        switch (n) {
            case 1:
                System.out.println("it's one");
                break;
            case 2:
                System.out.println("it's two");
                break;
            default:
                System.out.println("it's other");
        }
    }
}
