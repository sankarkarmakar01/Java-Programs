public class Pattern8 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (char ch = 'a'; ch <= 'e'; ch++) {
                System.out.print(ch + " ");
            }
            System.out.println();
        }
    }
}