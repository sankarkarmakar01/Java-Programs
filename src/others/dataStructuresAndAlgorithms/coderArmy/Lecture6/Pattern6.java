package others.dataStructuresAndAlgorithms.coderArmy.Lecture6;

public class Pattern6 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print((j * j) + " ");
            }
            System.out.println();
        }
    }
}